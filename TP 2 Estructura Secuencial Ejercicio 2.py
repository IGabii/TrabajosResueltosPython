"""Desarrollar un programa que permita ingresar dos números enteros A y B a travéz del teclado
Imprimir su suma y su diferencia"""

a=int(input("Ingrese un Número: "))
b=int(input("Ingrese otro Número: "))
resultado=a+b
diferencia=a-b
print("La suma de ambos números es: ",resultado, "Y la resta de ambos números es de: ",diferencia)
print(diferencia)